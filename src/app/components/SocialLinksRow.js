/**
 * Display a row of social media icons for a user profile
 * Only renders icons for platforms that have URLs set
 *
 * @param {object} props
 * @param {object} props.socialLinks - Object with platform keys and URL values
 * @param {string} props.className - Additional CSS classes
 */
export default function SocialLinksRow({ socialLinks, className = '' }) {
  // Filter to only active (non-empty) links
  const activeLinks = getActiveSocialLinks(socialLinks);

  // Don't render if no links
  if (!activeLinks || Object.keys(activeLinks).length === 0) {
    return null;
  }

  // Sort by platform order for consistent display
  const sortedPlatforms = SOCIAL_PLATFORM_ORDER.filter((platform) => activeLinks[platform]);

  return (
    <div
      className={`flex items-center gap-3 ${className}`}
      role="list"
      aria-label="Social media links"
    >
      {sortedPlatforms.map((platform) => {
        const url = activeLinks[platform];
        const Icon = SOCIAL_ICONS[platform];
        const platformInfo = SOCIAL_PLATFORMS[platform];

        if (!Icon || !url) return null;

        return (
          <a
            key={platform}
            href={url}
            target="_blank"
            rel="noopener noreferrer"
            role="listitem"
            className="group relative flex h-9 w-9 items-center justify-center rounded-lg border border-[var(--border-subtle)] bg-[var(--bg-elev-2)] text-[var(--text-tertiary)] transition-all duration-200 hover:border-red-500/50 hover:bg-red-500/10 hover:text-red-500 focus:outline-none focus:ring-2 focus:ring-red-500 focus:ring-offset-2 focus:ring-offset-[var(--bg-elev-1)]"
            aria-label={`Visit ${platformInfo?.name || platform} profile (opens in new tab)`}
            title={platformInfo?.name || platform}
          >
            <Icon className="h-4 w-4 transition-transform duration-200 group-hover:scale-110" />
          </a>
        );
      })}
    </div>
  );
}

/**
 * Skeleton loader for SocialLinksRow
 * Shows placeholder icons while loading
 *
 * @param {object} props
 * @param {number} props.count - Number of skeleton icons to show (default: 3)
 */
export function SocialLinksRowSkeleton({ count = 3 }) {
  return (
    <div className="flex items-center gap-3">
      {Array.from({ length: count }).map((_, i) => (
        <div key={i} className="h-9 w-9 animate-pulse rounded-lg bg-[var(--bg-elev-2)]" />
      ))}
    </div>
  );
}
